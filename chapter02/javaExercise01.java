package chapter02;

public class javaExercise01 {
    public static void main(String[] args) {
        //转义字符
        System.out.println("我在学习，学习使我快乐");
        System.out.println("我在学习，\t学习使我快乐");
        System.out.println("我在学习，\\学习使我快乐");
        System.out.println("我在学习，\'\'学习使我快乐");
        System.out.println("我在学习，\"\"学习使我快乐");
        System.out.println("我在学习，\n学习使我快乐");
        System.out.println("我在学习，学习使\r我快乐");
        System.out.println("我在学习，\r\n学习使我快乐");
        //课堂练习
        System.out.println("书名\t作者\t价格\t销量\n三国演义\t罗贯中\t120\t1000");
    }
}
